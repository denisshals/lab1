void LCD_Task() {
    LCD_Init();
    setDescription();
    for(;;) {
    	vTaskDelay(10000);
    	double output;
    	if(lcdMode == RMS) output = sqrt(value/valueCount);
    	else if(lcdMode == ampl) output = value;
    	else output = value/valueCount;
    	value = 0;
    	valueCount = 0;
    	setDouble(output);
    }
    vTaskDelete(NULL);
}
void button_Task() {
	for(;;) {
		vTaskSuspend(NULL);
		button_pin.previous_state = HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin);
		vTaskDelay(300);
		button_pin.current_state = HAL_GPIO_ReadPin(B1_GPIO_Port, B1_Pin);
		LCD_Clear();
		if(button_pin.current_state == GPIO_PIN_RESET && button_pin.previous_state == button_pin.current_state) {
			if(lcdMode == RMS) lcdMode = ampl;
			else if(lcdMode == ampl) lcdMode = ARV;
			else lcdMode = RMS;
			setDescription();
			value = 0;
			valueCount = 0;
		}
		button_pin.previous_state = GPIO_PIN_SET;
		button_pin.current_state = GPIO_PIN_SET;
		xSemaphoreGive(buttonMutex);
	}
	vTaskDelete(NULL);
}
void poll_Task() {
	for(;;) {
		HAL_ADC_Start_DMA(&hadc1, (uint32_t*) adcRaw, 2);
		vTaskSuspend(NULL);
		double I = 1.21  / adcRaw[0] * adcRaw[1] / 9200.0;
		if(lcdMode == RMS) {
			value += I * I;
			valueCount++;
		} else if(lcdMode == ampl){
			if(value < I) value = I;
		} else {
			value += I;
			valueCount++;
		}
	    TickType_t xLastWakeTime;
	    xLastWakeTime = xTaskGetTickCount();
	    vTaskDelayUntil(&xLastWakeTime, 1);
	}
	vTaskDelete(NULL);
}
void setDescription() {
	LCD_Clear();
	LCD_SetCursor(1);
	if(lcdMode == RMS) {
		uint8_t array[] = {0x43, 0x4B, 0x33};
		LCD_PrintString(array, 3);
	} else if(lcdMode == ampl) {
		uint8_t array[] = {0x41, 0xBC, 0xBE, 0xBB};
		LCD_PrintString(array, 4);
	} else {
		uint8_t array[] = {0x43, 0x42, 0x33};
		LCD_PrintString(array, 3);
	}
	LCD_SetCursor(7);
	uint8_t array[5] = {0x49, 0x3D};
	LCD_PrintString(array, 2);
}
void setDouble(double outputValue)  {
	LCD_SetCursor(9);
	LCD_PrintDouble(outputValue);
}
